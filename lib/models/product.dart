class Product {
  final String id;
  final String name;
  final String description;
  final String manufacturer;
  final String date;
  final double price;
  final String code;
  final bool status;
  final String image;
  final String userId;

  Product({
    required this.id,
    required this.name,
    required this.description,
    required this.manufacturer,
    required this.date,
    required this.price,
    required this.code,
    required this.status,
    required this.image,
    required this.userId,
  });
}
